<script>
    const sliderInner = document.getElementById('reviewSliderInner');
    const reviewBtnNext = document.getElementById('reviewBtnNext');
    const reviewBtnPrev = document.getElementById('reviewBtnPrev');
    let reviewItems = document.querySelectorAll('.review-item');

    // Setup for Infinite Loop
    const firstClone = reviewItems[0].cloneNode(true);
    const lastClone = reviewItems[reviewItems.length - 1].cloneNode(true);
    const secondClone = reviewItems[1].cloneNode(true); // Extra clone for 2-card desktop view

    sliderInner.appendChild(firstClone);
    sliderInner.appendChild(secondClone);
    sliderInner.prepend(lastClone);

    let reviewCounter = 1;
    let reviewIsTransitioning = false;

    function getReviewStepWidth() {
        return document.querySelector('.review-item').offsetWidth;
    }

    function setSliderPosition() {
        sliderInner.style.transition = "none";
        sliderInner.style.transform = `translateX(-${reviewCounter * getReviewStepWidth()}px)`;
    }

    // Initialize position
    window.addEventListener('load', setSliderPosition);
    window.addEventListener('resize', setSliderPosition);

    function moveReviewSlide(dir) {
        if (reviewIsTransitioning) return;
        reviewIsTransitioning = true;

        sliderInner.style.transition = "transform 0.6s cubic-bezier(0.4, 0, 0.2, 1)";
        reviewCounter += dir;
        sliderInner.style.transform = `translateX(-${reviewCounter * getReviewStepWidth()}px)`;
    }

    sliderInner.addEventListener('transitionend', () => {
        reviewIsTransitioning = false;

        // Loop back to real first item
        if (reviewCounter >= reviewItems.length + 1) {
            sliderInner.style.transition = "none";
            reviewCounter = 1;
            sliderInner.style.transform = `translateX(-${reviewCounter * getReviewStepWidth()}px)`;
        }

        // Loop back to real last item
        if (reviewCounter <= 0) {
            sliderInner.style.transition = "none";
            reviewCounter = reviewItems.length;
            sliderInner.style.transform = `translateX(-${reviewCounter * getReviewStepWidth()}px)`;
        }
    });

    reviewBtnNext.addEventListener('click', () => moveReviewSlide(1));
    reviewBtnPrev.addEventListener('click', () => moveReviewSlide(-1));

    // Auto Play Logic
    let reviewAutoPlay = setInterval(() => moveReviewSlide(1), 4000);
    const wrapper = document.querySelector('.review-slider-wrapper');
    wrapper.onmouseenter = () => clearInterval(reviewAutoPlay);
    wrapper.onmouseleave = () => reviewAutoPlay = setInterval(() => moveReviewSlide(1), 4000);
</script>


<script>
    const slider = document.getElementById('reelSlider');
    let index = 0;
    let isPaused = false;

    // Auto slide function
    function autoSlide() {
        if (isPaused) return; // Pause slide when user interacts

        const wrappers = document.querySelectorAll('.reel-wrapper');
        const totalItems = wrappers.length;
        const itemWidth = wrappers[0].offsetWidth;

        let visibleCount = 1;
        if (window.innerWidth >= 992) visibleCount = 3;
        else if (window.innerWidth >= 768) visibleCount = 2;

        index++;

        if (index > totalItems - visibleCount) {
            index = 0;
        }

        slider.style.transform = `translateX(-${index * itemWidth}px)`;
    }

    let slideInterval = setInterval(autoSlide, 4000);

    // Resize fix
    window.addEventListener('resize', () => {
        slider.style.transform = `translateX(0px)`;
        index = 0;
    });

    // Optional: Stop auto-slide on hover
    const reelSection = document.querySelector('.reels-section');
    reelSection.addEventListener('mouseenter', () => isPaused = true);
    reelSection.addEventListener('mouseleave', () => isPaused = false);
</script>





